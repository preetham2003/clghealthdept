    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
                                            
     
                    
                                                                       
                                       
    // Get the values from the text fields
    String studentName = jTextField1.getText();
    String usn = jTextField10.getText();
    String gender = (String) jComboBox1.getSelectedItem();
    String contact = jTextField3.getText();
    String age = jTextField4.getText();
    String caseType = jTextField5.getText();
    String referral = jTextField6.getText();
    String chiefComplaints = jTextField7.getText();
    String intervention = jTextField8.getText();
    String medicineId = jTextField2.getText(); // Assuming jTextField9 contains the medicine ID
    
    // Check if the USN already exists in the students table
    boolean usnExists = checkIfUSNExists(usn);
    
    if (usnExists) {
        JOptionPane.showMessageDialog(this, "USN already exists in the database.");
    } else {
        // Insert data into students table
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/clghealthdpt", "root", "14ec8d7b");
            PreparedStatement stmt = conn.prepareStatement("INSERT INTO students (sname, USN, Gender, Contact, Age) VALUES (?, ?, ?, ?, ?)");
            stmt.setString(1, studentName);
            stmt.setString(2, usn);
            stmt.setString(3, gender);
            stmt.setString(4, contact);
            stmt.setString(5, age);
            stmt.executeUpdate();
            conn.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error inserting data into students table.");
            return;
        }
    }
        
    // Insert data into cases table
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/clghealthdpt", "root", "14ec8d7b");
        PreparedStatement stmt = conn.prepareStatement("INSERT INTO health_issues (USN, CaseType, Referral, ChiefComplaints, Intervention) VALUES (?, ?, ?, ?, ?)");
        stmt.setString(1, usn);
        stmt.setString(2, caseType);
        stmt.setString(3, referral);
        stmt.setString(4, chiefComplaints);
        stmt.setString(5, intervention);
        int affectedRows = stmt.executeUpdate();
        
        if (affectedRows == 0) {
            throw new SQLException("Creating health issue failed, no rows affected.");
        }

        // Retrieve the case number for the inserted record
        int caseNo = -1;
        PreparedStatement caseNoStmt = conn.prepareStatement("SELECT CASENO FROM health_issues WHERE USN = ? ORDER BY CASENO DESC LIMIT 1");
        caseNoStmt.setString(1, usn);
        ResultSet caseNoResult = caseNoStmt.executeQuery();
        if (caseNoResult.next()) {
            caseNo = caseNoResult.getInt("CASENO");
        }

        if (caseNo == -1) {
            throw new SQLException("Failed to retrieve case number.");
        }

        // Insert data into uses table
        PreparedStatement usesStmt = conn.prepareStatement("INSERT INTO uses (caseno, medicine_id) VALUES (?, ?)");
        usesStmt.setInt(1, caseNo);
        usesStmt.setString(2, medicineId);
        usesStmt.executeUpdate();

        conn.close();
        JOptionPane.showMessageDialog(this, "Data inserted successfully into both tables.");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error inserting data into healthissues or uses table: " + e.getMessage());
        return;
    }


    }//GEN-LAST:event_jButton5ActionPerformed

