    private boolean checkIfUSNExists(String usn) {
    boolean exists = false;
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/clghealthdpt", "root", "14ec8d7b");
        PreparedStatement stmt = conn.prepareStatement("SELECT * FROM students WHERE USN = ?");
        stmt.setString(1, usn);
        ResultSet rs = stmt.executeQuery();
        if (rs.next()) {
            exists = true; // USN already exists in the table
        }
        conn.close();
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error checking if USN exists.");
    }
    return exists;
}


